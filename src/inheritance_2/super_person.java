public class super_person {
    
    private String  id, name , addres;

    public super_person(String id, String name, String addres) {
        this.id = id;
        this.name = name;
        this.addres = addres;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getAddres() {
        return addres;
    }
    
    
}
